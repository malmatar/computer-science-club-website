	<div id="slides"><br/><br/>The Association of Computing Machinery (or ACM) is the world’s largest international membership organization dedicated to computing. Founded in 1947, the ACM has matured into an organization made up of over 100,000 members and more than 600 local and university chapters. The ACM is well-known for its numerous publications that offer unique perspectives from leading professionals in the computing industry. It is also well-known for its Digital Library, the world’s most comprehensive collection of computing materials, including millions of pages of full-text articles, journals, and conference proceedings dating back to the 1950s.
	
	<br/><br/>The Computer Science Society (CSS) at Northeastern Illinois University is one of the ACM’s many University Chapters. Chartered during the summer of 2013, we have strived to uphold the mission of the ACM by delivering learning materials and professional opportunities in computer science to students in the NEIU community. We are made up of NEIU’s most obsessed computer science enthusiasts and we are thrilled at every opportunity we get to learn about something new in computing. Many of NEIU’s graduate and undergraduate computer science majors belong to the CSS, but we welcome all NEIU students and members of the ACM to join us in our weekly activities.
	
	<br/><br/>What kind of things do we do we do in the CSS? Well, we often find ourselves engaged in things like programming projects or heated discussions about computing technologies, but we have also been known to break out the board games and game consoles and watch movies for fun. If you are interested in joining an exciting and diverse group of learners engaged in computer science, you don’t have to look any further! We are the one and only NEIU computer science club.
		
